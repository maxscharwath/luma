/// Attach resolved TMDB metadata to one item (used by the enrichment pass).
pub fn set_item_metadata(pool: &Pool, id: &str, meta: &Metadata) -> Result<()> {
    let conn = pool.get()?;
    let json = serde_json::to_string(meta)?;
    conn.execute("UPDATE items SET metadata = ?2 WHERE id = ?1", params![id, json])?;
    Ok(())
}

/// Dual-write the resolved *localized* metadata for one catalog subject (a movie
/// item or a show) into the language-agnostic cache: the invariant
/// [`metadata_core`] row once, plus a [`translations`] row per language.
///
/// `core_meta` is the localized primary-language [`Metadata`] its art URLs are
/// the cached local paths and its `cast` carries the (invariant) localized
/// photos, so it's the right source for the core row. `by_lang` supplies the
/// per-language text; a language's character names align by index to `core_meta`'s
/// cast (TMDB returns the same cast order in every language). All writes share one
/// connection. This runs *alongside* the legacy `metadata` blob write during the
/// transition; the read path keeps using the blob until the cutover.
pub fn store_localized(
    pool: &Pool,
    kind: &str,
    id: &str,
    core_meta: &Metadata,
    by_lang: &HashMap<String, Metadata>,
) -> Result<()> {
    use super::metadata_core::{self, MetaCore};
    use super::translations::{self, TransData};
    let conn = pool.get()?;
    let core = MetaCore {
        // `Metadata::tmdb_id` is 0 when unresolved; store NULL for that.
        tmdb_id: (core_meta.tmdb_id != 0).then_some(core_meta.tmdb_id),
        imdb_id: core_meta.imdb_id.clone(),
        tvdb_id: core_meta.tvdb_id,
        release_date: core_meta.release_date.clone(),
        rating: core_meta.rating,
        poster_url: core_meta.poster_url.clone(),
        backdrop_url: core_meta.backdrop_url.clone(),
        logo_url: core_meta.logo_url.clone(),
        cast: core_meta.cast.clone(),
        crew: core_meta.crew.clone(),
    };
    metadata_core::write_core(&conn, kind, id, &core)?;
    for (lang, m) in by_lang {
        let data = TransData {
            title: m.title.clone(),
            tagline: m.tagline.clone(),
            overview: m.overview.clone(),
            genres: m.genres.clone(),
            characters: m.cast.iter().map(|c| c.character.clone()).collect(),
            reason: None,
        };
        if !data.is_empty() {
            translations::write(&conn, kind, id, lang, translations::TMDB, &data)?;
        }
    }
    Ok(())
}

/// Wipe all resolved TMDB metadata so the next enrichment re-fetches from
/// scratch: clears item/show metadata JSON, season casts and title embeddings.
/// Returns `(items_cleared, shows_cleared)`. The process-wide lookup cache must
/// be cleared separately by the caller (see `crate::infra::metadata::Cache`).
pub fn reset_all_metadata(pool: &Pool) -> Result<(usize, usize)> {
    let conn = pool.get()?;
    let items = conn.execute("UPDATE items SET metadata = NULL WHERE metadata IS NOT NULL", [])?;
    let shows = conn.execute("UPDATE shows SET metadata = NULL WHERE metadata IS NOT NULL", [])?;
    conn.execute("DELETE FROM item_vectors", [])?;
    conn.execute("DELETE FROM season_meta", [])?;
    // Language-agnostic cache: drop the invariant cores and every TMDB-sourced
    // translation (movie/show/episode/season_cast). LLM-sourced rows (curated
    // sections, AI suggestions) are left intact they're regenerated by their own
    // jobs, not by TMDB enrichment.
    conn.execute("DELETE FROM metadata_core", [])?;
    conn.execute("DELETE FROM translations WHERE source = 'tmdb'", [])?;
    Ok((items, shows))
}

/// Store a season's TMDB cast (used by the enrichment pass). Upsert.
pub fn set_season_cast(pool: &Pool, show_id: &str, season: u32, cast: &[CastMember]) -> Result<()> {
    let conn = pool.get()?;
    let json = serde_json::to_string(cast)?;
    conn.execute(
        "INSERT INTO season_meta (show_id,season,casts) VALUES (?1,?2,?3) \
         ON CONFLICT(show_id,season) DO UPDATE SET casts=excluded.casts",
        params![show_id, season, json],
    )?;
    Ok(())
}

/// Which of a show's seasons already have a stored cast (so re-scans skip them).
pub fn seasons_with_cast(pool: &Pool, show_id: &str) -> Result<std::collections::HashSet<u32>> {
    let conn = pool.get()?;
    let mut stmt = conn.prepare("SELECT season FROM season_meta WHERE show_id = ?1")?;
    let rows = stmt.query_map(params![show_id], |r| r.get::<_, u32>(0))?;
    Ok(rows.collect::<rusqlite::Result<std::collections::HashSet<_>>>()?)
}

/// A show's per-season cast, keyed by season number (for `get_show`).
pub fn season_casts(pool: &Pool, show_id: &str) -> Result<HashMap<u32, Vec<CastMember>>> {
    let conn = pool.get()?;
    let mut stmt = conn.prepare("SELECT season,casts FROM season_meta WHERE show_id = ?1")?;
    let rows = stmt.query_map(params![show_id], |r| {
        Ok((r.get::<_, u32>(0)?, r.get::<_, String>(1)?))
    })?;
    let mut out = HashMap::new();
    for row in rows {
        let (season, json) = row?;
        if let Ok(cast) = serde_json::from_str::<Vec<CastMember>>(&json) {
            out.insert(season, cast);
        }
    }
    Ok(out)
}

